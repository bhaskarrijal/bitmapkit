const char *bk_exif_registry_name(uint32_t code) {
  for (size_t i = 0; i < sizeof(exif_registry) / sizeof(exif_registry[0]);
       ++i) {
    if (exif_registry[i].code == code)
      return exif_registry[i].name;
  }
  return "Unknown EXIF tag";
}

