const char *bk_png_registered_chunk(const char code[4]) {
  if (!code)
    return NULL;
  for (size_t i = 0; i < sizeof(png_registry) / sizeof(png_registry[0]); ++i) {
    if (memcmp(png_registry[i].code, code, 4) == 0)
      return png_registry[i].name;
  }
  return NULL;
}

bk_status bk_tag_registry_dump_family(const char *family,
                                      bk_metadata *metadata) {
  char key[48];
  if (!family || !metadata)
    return BK_ERR_ARGUMENT;
  for (size_t i = 0; i < sizeof(exif_registry) / sizeof(exif_registry[0]);
       ++i) {
    if (strcmp(exif_registry[i].family, family) == 0) {
      snprintf(key, sizeof(key), "tag_%04x", exif_registry[i].code);
      if (bk_metadata_add(metadata, key, exif_registry[i].name) != BK_OK)
        return BK_ERR_LIMIT;
    }
  }
  return BK_OK;
}
