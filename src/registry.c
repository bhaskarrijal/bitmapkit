const char *bk_registry_mime_for_id(const char *id) {
  if (!id)
    return NULL;
  for (size_t i = 0; i < sizeof(format_rules) / sizeof(format_rules[0]); ++i) {
    if (strcmp(format_rules[i].id, id) == 0)
      return format_rules[i].mime;
  }
  return NULL;
}

const char *bk_registry_label_for_id(const char *id) {
  if (!id)
    return NULL;
  for (size_t i = 0; i < sizeof(format_rules) / sizeof(format_rules[0]); ++i) {
    if (strcmp(format_rules[i].id, id) == 0)
      return format_rules[i].label;
  }
  return NULL;
}

uint32_t bk_registry_field_count(const char *format) {
  uint32_t count = 0;
  if (!format)
    return 0;
  for (size_t i = 0; i < sizeof(field_rules) / sizeof(field_rules[0]); ++i) {
    if (strcmp(field_rules[i].format, format) == 0)
      ++count;
  }
  return count;
}

bk_status bk_registry_describe_format(const char *id, bk_metadata *metadata) {
  if (!id || !metadata)
    return BK_ERR_ARGUMENT;
  for (size_t i = 0; i < sizeof(format_rules) / sizeof(format_rules[0]); ++i) {
    if (strcmp(format_rules[i].id, id) == 0) {
      bk_metadata_add(metadata, "format", format_rules[i].label);
      bk_metadata_add(metadata, "mime", format_rules[i].mime);
      bk_metadata_add(metadata, "signature", format_rules[i].signature);
      bk_metadata_add(metadata, "notes", format_rules[i].notes);
      return BK_OK;
    }
  }
  return BK_ERR_BAD_MAGIC;
}

bk_status bk_registry_describe_fields(const char *format,
                                      bk_metadata *metadata) {
  char key[48];
  if (!format || !metadata)
    return BK_ERR_ARGUMENT;
  for (size_t i = 0; i < sizeof(field_rules) / sizeof(field_rules[0]); ++i) {
    if (strcmp(field_rules[i].format, format) == 0) {
      snprintf(key, sizeof(key), "field_%zu", metadata->count);
      if (bk_metadata_add(metadata, key, field_rules[i].name) != BK_OK)
        return BK_ERR_LIMIT;
    }
  }
  return BK_OK;
}
